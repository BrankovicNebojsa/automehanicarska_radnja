    private void jButtonDodajModelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajModelActionPerformed
        if (isDataValid()) {
            jTextFieldNazivMarke.setEditable(false);
            if (marka.getNazivMarke().equals("")) {
                String nazivMarke = jTextFieldNazivMarke.getText().trim();
                marka.setNazivMarke(nazivMarke);
            }
            Model model = new Model();
            model.setNazivModela(jTextFieldNazivModela.getText().trim());
            model.setMarka(new Marka(marka.getNazivMarke()));
            modeli.add(model);
            marka.setModeli(modeli);
            ((AddMarkaTableModel) jTableModeli.getModel()).setMarka(marka);
        }
    }//GEN-LAST:event_jButtonDodajModelActionPerformed


    private void jButtonZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZapamtiActionPerformed
        if (marka.getModeli().size() > 0) {
            if (c.saveMarka(marka)) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvana marka");
                this.setVisible(false);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Unesite modele");
        }
    }//GEN-LAST:event_jButtonZapamtiActionPerformed

